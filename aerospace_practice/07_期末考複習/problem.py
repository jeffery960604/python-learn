'''第三題：零件庫存盤點 (for 迴圈走訪列表)**
*   **情境**：實驗室有一批零件送來了，清單是 `parts = ["螺絲", "墊片", "鈦合金板", "感測器"]`。
*   **任務**：請用 `for` 迴圈把 `parts` 裡面的東西一個一個拿出來，並印出 `"正在清點零件：[零件名稱]"`。'''

parts = ["螺絲", "墊片", "鈦合金板", "感測器"]
for part in parts:
    print(f"正在清點零件：{part}")

'''**第四題：風洞測試倒數 (while 迴圈)**
*   **情境**：風洞啟動前需要倒數 3 秒。
*   **任務**：設定一個變數 `timer = 3`。寫一個 `while` 迴圈，當 `timer` 大於 0 時，印出 `"風洞啟動倒數：X 秒"`，然後把 `timer` 減 1。迴圈結束後，印出 `"風洞啟動！"`。'''

timer=3
while timer>0:
    print(f"風洞啟動倒數：{timer} 秒")
    timer -=1
print("風洞啟動！")

'''第五題：尋找故障的火星塞 (for 迴圈 + if 判斷式 + break)**
*   **情境**：我們在測試一排火星塞，狀態數據是 `spark_plugs = ["OK", "OK", "FAIL", "OK", "OK"]`。
*   **任務**：用 `for` 迴圈檢查每個狀態。
    *   如果狀態是 `"OK"`，印出 `"火星塞正常"`。
    *   如果狀態是 `"FAIL"`，印出 `"警告：發現故障的火星塞！立即停止測試。"`，**並且立刻中斷迴圈**（後面剩下的都不檢查了）。'''

spark_plugs = ["OK", "OK", "FAIL", "OK", "OK"]
for spark in spark_plugs:
    if spark == 'OK':
        print('火星塞正常')
    else :
        print("警告：發現故障的火星塞！立即停止測試。")  
        break







