'''第一題：無人機電量檢查 (單純判斷式)

情境：設定一個變數 battery 代表無人機的電量（例如 battery = 15）。
任務：寫一個 if-else 判斷式。如果電量大於等於 20，印出 "電量充足，繼續巡航"；如果小於 20，印出 "電量過低，請立即返航"。'''


battery=int(input("請輸入電池電量："))
if (battery>=20):
    print("電量充足，繼續巡航")
else:
    print("電量過低，請立即返航")

